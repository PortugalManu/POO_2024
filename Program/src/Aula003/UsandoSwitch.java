package Aula003;

public class UsandoSwitch {

	public static void main(String[] args) {
		for (int i = 1; i < 5; i++) {
			switch (i) {
			case 1:
				System.out.println("Numero 1");
				break;
			case 2:
				System.out.println("Numero 2 ");
				break;
			default:
				System.out.println("Numero invalido");

			}
		}
	}

}
